	void SchalteLEDDuo(MobaLedLib_C& mobaLedLib, uint8_t xArt, inch_t yDstVar1, inch_t yDstVarN, uint8_t yZufallA, uint8_t yZufallB, uint8_t yRandom)
	//-------------------------------------------------------------------------------------------------------------------------------------------
	// xArt 1 = einschalten, 2 = ausschalten
	{
		uint8_t yINP = 0;
		uint8_t yTurned = 0;
		uint8_t yschalt_A_B = 0;
		uint8_t ySet = 0;
		uint8_t ymyLed = 0;
		if (xArt == 1)								// ist Off, soll ein
		{
			yINP = INP_OFF;
			yTurned = INP_TURNED_OFF;
			ySet = 1;
			ymyLed = myLedOn;
		} else 										// ist On, soll Off
		{
			yINP = INP_ON;
			yTurned = INP_TURNED_ON;
			ySet = 0;
			ymyLed = myLedOff;
		}
		for ( DstVar = yDstVar1; DstVar <= yDstVarN; DstVar ++)
		{
			yschalt_A_B = IfMyTime();
			if (yschalt_A_B > 0)
			{
				if (mobaLedLib.Get_Input(DstVar) == yINP || mobaLedLib.Get_Input(DstVar) == yTurned)
				{
					if (yRandom-- == 0)
					{
#ifdef _PeterDebugDuo
						SchaltzeitInfo( DstVar, ymyLed, yZufallA, yZufallB, yschalt_A_B);
#endif		
						mobaLedLib.Set_Input(DstVar,ySet);
						if (yschalt_A_B == 1)
						{
							if (yZufallA == 0)		// 02.12.2025
							{
								Pausentimer();		// 02.12.2025
							}
							ToDoCnt --;
							if (ToDoCnt > 0)
							{
								SwitchValA = SwitchValA + yZufallA;
								if (SwitchValA > 511)
								{
									SwitchValA = yZufallA;
								}
							}
						}
						if (yschalt_A_B == 2)
						{
							if (yZufallB == 0)		// 02.12.2025
							{
								Pausentimer();		// 02.12.2025
							}
							ToDoCnt --;
							if (ToDoCnt > 0)
							{
								SwitchValB = SwitchValB + yZufallB;
								if (SwitchValB > 511)
								{
									SwitchValB = yZufallB;
								}
							}
						}
					} 
				} 
			}
			yschalt_A_B = 0;
		}
	}	




	//------------------------------------------
	void Proc_ScheduleDuo(MobaLedLib_C& mobaLedLib)
	//------------------------------------------
	{
		inch_t DstVar1;
		inch_t DstVarN;
		uint8_t nRandom;
		uint8_t xzufallA = 2;
		uint8_t xzufallB = 2;
		uint16_t xDiff;
		uint8_t schalt_A_B = 0;
		uint8_t getInput;							// = mobaLedLib.Get_Input(enableCh); 		// Input LED-Nummer ????

		DstVar1 = dstVar1;
		DstVarN = dstVarN;
		getInput = mobaLedLib.Get_Input(enableCh); 	// Input #InCh --> bringt 3 zurück
		if (Inp_Is_On(getInput))
		{
			if (ToDoCnt == 0)  
			{
				ToDoCntSum = 0;
				for (DstVar = DstVar1; DstVar <= DstVarN; DstVar ++)	// Zählen der aktiven LED's	// 0.19
				{
						ToDoCnt ++;
						ToDoCntSum ++;				// 02.12.2025
				}
				CalcSwitchVal(my_FirstLedStatus);
			}
			if (ToDoCnt > 0)  						// 02.12.2025
			{
				if (DstVarN - DstVar1 > 0)
				{
					xzufallA = zufallA / ToDoCntSum  ;		// Sind mehrere Objekte vorhanden, so wird der geforderte Zufallswert duch die Anzahl der Objekte. 28.11.2025
					xzufallB = zufallB / ToDoCntSum  ;
					xzufallA = P_DiffCalc(my_startA, my_endA, xzufallA);
					xzufallB = P_DiffCalc(my_startB, my_endB, xzufallB);
				}
				else
				{
					xzufallA = 0;					// Bei nur einem Objekt ist die Berechnung nicht notwendig.
					xzufallB = 0;	
				}
				nRandom = random8(ToDoCnt);
				if (my_FirstLedStatus == myLedOff)	// LED ist noch aus
				{
					SchalteLEDDuo( mobaLedLib, 1, DstVar1, DstVarN, xzufallA, xzufallB, nRandom);
				}
				else    							// LED ist noch ein 	
				{
					SchalteLEDDuo( mobaLedLib, 2, DstVar1, DstVarN, xzufallA, xzufallB, nRandom);
				}
				if (ToDoCnt == 0)
				{
					if (my_FirstLedStatus == myLedOff)
					{
						my_FirstLedStatus = myLedOn ;
					}
					else
					{
						my_FirstLedStatus = myLedOff ;
					}
				}
			} 
		}
	}

 
