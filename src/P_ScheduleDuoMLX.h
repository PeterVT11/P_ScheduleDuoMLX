	//--------------------------------------
	void Pausentimer()								// 02.12.2025
	//--------------------------------------
	// Wenn xZufall == 0 ist, wird eine Zwangspause zwischen 2 Schaltungen eingefügt von 100 mSec.
	{
		unsigned long pTime1 = millis();
		unsigned long pTime2 = pTime1 + 100;
		do // Schleife für die Verzögerungszeit
		{  //   
			pTime1 = millis();
		} while (pTime1 < pTime2);
	}
	

	//--------------------------------------
	uint8_t P_RundenMinTakt(uint8_t Minuten)			
	//--------------------------------------
	// Teile Minuten durch 3 und runde.
	// Das Runden der Minuten geschieht aus Speicherplatz-Gründen sehr einfach.
	{
		uint8_t minuteRund = 0;
		minuteRund = Minuten/3;
		if((minuteRund % 3) >= 2) minuteRund++;
		return minuteRund;
 	}
	
	//--------------------------------------------------
	uint16_t Uhr2Tick( uint8_t x_stunde, uint8_t x_minute)
	//--------------------------------------------------
	// Umrechnung der Uhrzeit in MLL-Ticks 
	{
		uint16_t takt = 0;
		takt = x_stunde * 21;
		if (x_stunde > 2) takt ++;
		if (x_stunde > 6) takt ++;
		if (x_stunde > 10) takt ++;
		if (x_stunde > 12) takt ++;
		if (x_stunde > 13) takt ++;
		if (x_stunde > 17) takt ++;
		if (x_stunde > 21) takt ++;
		takt = takt + P_RundenMinTakt(x_minute);
		if (x_minute > 29) takt ++;
		return takt;
	}

	//--------------------------------------
	void CalcSwitchVal( uint8_t LedIs_OnOff)
	//--------------------------------------
	// Schaltzeit durch "Zufall" ermitteln. 
	// Es werden 512 hinzugezählt, um einen negativen Übelauf (Schaltezit - Zufall) zu umgehen.
	// Nach der Rundung werden diese wieder abgezogen. Ist das Ergebnis < 512 ist es der gerechnete neue Schaltwert.
	{
		uint16_t tempSchalt = 0;
		if (LedIs_OnOff == myLedOff)				// LED ist Off  -- Start wird berechnet
		{
			SwitchValA = random(512 + my_startA - zufallA, 512 + my_startA + zufallA);			//	###
			SwitchValB = random(512 + my_startB - zufallB, 512 + my_startB + zufallB);
		} 
		else 
		{											// LED ist On -- Ende wird berechnet		####
			SwitchValA = random(512 + my_endA - zufallA, 512 + my_endA + zufallA);
			SwitchValB = random(512 + my_endB - zufallB, 512 + my_endB + zufallB);
		}
		if (SwitchValA >= 512)						
		{
			SwitchValA = SwitchValA - 512;
			if (SwitchValA >= 512)
			{
				SwitchValA = SwitchValA - 512;
			}
			if (SwitchValA == 0)
			{
				SwitchValA = 1;
			}
		}
		if (SwitchValB >= 512)
		{
			SwitchValB = SwitchValB - 512;
			if (SwitchValB >= 512)
			{
				SwitchValB = SwitchValB - 512;
			}
			if (SwitchValB == 0)
			{
				SwitchValB = 1;
			}
		}
		else
		{
			SwitchValA = SwitchValA;				// Unnötig, nur zur Klarheit.
			SwitchValB = SwitchValB;
		}
		if (neuStart == true) 						// V0.20 Wenn NeuStart aktiv, dann prüfen, ob Einschalt-Zeitpunkt vorher war, aber Ausschaltpunkt noch nicht erreicht.
		{
			if (my_startA < 256) 					// Start vor Mittag (12:00) für 
			{
				if (my_endA > 256) {					// Ende nach Mittag (12:00)
					SwitchValA = MyAktTime + 1;		// Zur Sicherheit 1 MInute zugeben zu aktueller Schaltzeit.
				}	
			}
			if (my_startB < 256) 					// Start vor Mittag (12:00)
			{
				if (my_endB > 256) 					// Ende nach Mittag (12:00)
				{
					SwitchValB = MyAktTime + 1;		// Zur Sicherheit 1 MInute zugeben zu aktueller Schaltzeit.
				}	
			}
			neuStart = false;						// NeuStart-Flag zurücksetzen, da nur einmal benötigt.
		}
	}
	 
