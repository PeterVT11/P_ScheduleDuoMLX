	uint8_t BereichPruefung(uint16_t start1, uint16_t ende1, uint16_t start2, uint16_t ende2, uint8_t yZufallA, uint8_t yZufallB) 
	//---------------------------------------------------------------------------------------------------------------------------
	// Prüft, ob sich Zeitbereiche der A_ und B-Zeit überschneiden (einschliesslich des Zufalls). Wenn ja, Ausgabe über serielle Schnittstelle.
	// xReturn ist das Ergebnis der Prüfung:
	// 		0 = alles ok
	//      1 = Start2 liegt im Bereich von Start2/Ende2 (Start vor Ende)
	//      2 = Ende2 liegt im Bereich von Start2/Ende2 (Start vor Ende)
	//     10 = Start2 liegt im Bereich von Start2/Ende2 (Start vor Ende, Start vor Ende, Rechnung bis Mitternacht)
	//     20 = Ende2 liegt im Bereich von Start2/Ende2 (Start vor Ende, Rechnung bis Mitternacht)
	//    100 = Start2 liegt im Bereich von Start2/Ende2 (Start nach Ende, Rechnung ab Mitternacht)
	//    200 = Ende2 liegt im Bereich von Start2/Ende2 (Start vor Ende, Rechnung ab Mitternacht)
	{
		uint8_t xReturn = 0;
	#ifdef _PeterDebugDuo_Intern	
		Serial << "BereichPruefung Inp " << start1 << " - "  << ende1 << " Zufall " << yZufallA << "  -  " << start2  << " - " << ende2 << " Zufall " << yZufallB << endl;  
	#endif
		start1 = 512 + start1 - yZufallA;
		if (start1 > 512)
		{
			start1 = start1 - 512;
		}
		ende1 = 512 + ende1 + yZufallA;
		if (ende1 > 512)
		{
			ende1 = ende1 - 512;
		}
		start2 = 512 + start2 - yZufallB;
		if (start2 > 512)
		{
			start2 = start2 - 512;
		}
		ende2 = 512 + ende2 + yZufallB;
		if (ende2 > 512)
		{
			ende2 = ende2 - 512;
		}
	#ifdef _PeterDebugDuo_Intern	
		Serial << "BereichPruefung Out " << start1 << " - "  << ende1 << "  " << start2  << " - " << ende2 << " (mit Zufall gerechnet)." << endl;  
	#endif	
		if (start1 < ende1)							// Startzeit vor Endzeit
		{
			for (uint16_t i = start1; i <= ende1; i++)
			{
				if (i == start2)
				{
					xReturn = xReturn + 1;				
					break;
				}
				if (i == ende2)
				{
					xReturn =  xReturn + 2;
					break;
				}
			}
		} else										// Startzeit nach Endzeit
		{
			for (uint16_t i = start1; i <= 512; i++)
			{
				if (i == start2)
				{
					xReturn =  xReturn + 10;
					break;
				}
				if (i == ende2)
				{
					xReturn = xReturn + 20;
					break;
				}
			}
			for (uint16_t i = 1; i <= ende1; i++)
			{
				if (i == start2)
				{
					xReturn =  xReturn + 100;
					break;
				}
				if (i == ende2)
				{
					xReturn =  xReturn + 200;
					break;
				}
			}
		}
		return xReturn;								// Ergebnis, siehe oben.
	}

	void FehlerHeader()
	{
		char s[100];
		sprintf(s, "P_ScheduleDuo: Zeit A: %02d:%02d - %02d:%02d, Zeit B: %02d:%02d - %02d:%02d\n", startA_Std, startA_Min, endA_Std, endA_Min, startB_Std, startB_Min, endB_Std, endB_Min);
		Serial.write (s);
	}



	//----------------------------------
	void setup(MobaLedLib_C& mobaLedLib) 
	//----------------------------------
	// Setup der Funktion
	{
	bool bFehlerHeader = false;
    // hier kommt der Code hin, der beim Starten ausgeführt wird
	uint8_t nErg = 0;
	
#ifdef _PeterDebugDuo
	flagMitternacht = 0;
	Serial << "P_D- P_ScheduleDuo Version " << MyProgDuo_MSG <<  endl;
#endif	
	if (neuzufallA != orgzufallA)
	{
		if (bFehlerHeader == false) 
		{
			FehlerHeader();
			bFehlerHeader = true;
		}
		char s[100];
		sprintf(s, "!!! P_ScheduleDuo:  Zufall-Wert A für LED: %d  verkürzt von %d auf %d Ticks\n", int(dstVar1), int(orgzufallA), int(neuzufallA));
		Serial.write(s);
		Serial.flush();
	}
	if (neuzufallB != orgzufallB)
	{
		if (bFehlerHeader == false) 
		{
			FehlerHeader();
			bFehlerHeader = true;
		}
		char s[100];
		sprintf(s, "!!! P_ScheduleDuo:  Zufall-Wert B für LED: %d  verkürzt von %d auf %d Ticks\n", int(dstVar1), int(orgzufallB), int(neuzufallB));
		Serial.write(s);
		Serial.flush();
	}
	nErg = BereichPruefung( my_startA, my_endA, my_startB, my_endB, zufallA, zufallB);
	if ( nErg  > 0)
	{
		if (bFehlerHeader == false) 
		{
			FehlerHeader();
			bFehlerHeader = true;
		}
		char s[100];
		sprintf(s, "!!! P_ScheduleDuo: Zeitbereiche überschneiden sich! (%d)\n", nErg);
		Serial.write(s);
		sprintf(s, "!!! P_ScheduleDuo: Zeit A: %02d:%02d - %02d:%02d  Zufall: +/- %d Ticks \n", (int)startA_Std, (int)startA_Min, (int)endA_Std, (int)endA_Min, (int)zufallA);
		Serial.write(s); 
		sprintf(s, "!!! P_ScheduleDuo: Zeit B: %02d:%02d - %02d:%02d  Zufall: +/- %d Ticks \n", (int)startB_Std, (int)startB_Min, (int)endB_Std, (int)endB_Min, (int)zufallB);
		Serial.write(s);
		Serial.flush();
	}
	if (bFehlerHeader == true)
	{
		Serial.write("---\n");
		Serial.flush();
	}
}


