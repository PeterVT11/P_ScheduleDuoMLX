	//-----------------------------------------------------------------
	uint8_t P_DiffCalc(uint16_t ystart, uint16_t yend, uint8_t yzufall)
	//-----------------------------------------------------------------
	// Prüfe, ob zwischen Start und Ende die Differenz 2x Zufall ist.
	// Falls nicht, so setzte den Zufall auf 1. Ist der ZUfall kleiner als
	// die Zahl der Objekte, so setze Zufall auf 0.
	{
		uint16_t ydiff = 0;
		if (yend > ystart)							// my_start: 8:00 (170) , my_end: 10:00 (213) = 43
		{
			ydiff = yend - ystart;
		} else										// my_start: 10:00 (213), my_end: 8:00 (170) =
		{
			ydiff = ystart - yend;
			ydiff = ydiff *-1;
		}
		if (ydiff <= (yzufall * 2))
		{
			yzufall = 1;
		}
		if (ydiff <= ToDoCntSum)
		{
			yzufall = 0;
		}
		return yzufall;
	}

	//----------------
	uint8_t IfMyTime() 
	//----------------
	// prüft, ob für Schaltzeit A oder B der Schaltpunkt erreicht ist.
	// Wennja wird 1 für Schaltzeit A, 2 für Schaltzeit B zurück gegeben, ansonsten 0.
	{
		uint8_t nErg = 0;
		if (MyAktTime == SwitchValA) 
		{
			nErg = 1;
		}
		if (MyAktTime == SwitchValB)
		{ 
			nErg = 2;
		}
		return nErg;
	}

